<script>
	export default {
		computed: {
			isOnboarding() {
				return this.$store.state.onboardingProcess;
			},
			load: false,
		},

		methods: {
			onboard() {
				this.load = true;
				this.$store.commit('storeOnboarding', false);

				this.$http.post('user/onboarded')
					.then(() => {
						this.load = false;

						const tmpUser = this.$auth.user();
						tmpUser.is_onboarded = 1;
						this.$auth.user(tmpUser);
						window.localStorage.setItem('userData', JSON.stringify(tmpUser));

						this.$router.push('/tutorial');
					})
					.catch((error) => {
						this.load = false;
						this.$root.catchError(error.response);
					});
			},
		},
	};
</script>

